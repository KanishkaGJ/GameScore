package com.score;

public class highScore {
	
	private int id;
	private int gid;
	private int score;
	
	
	public highScore(int id, int gid, int score) {
		super();
		this.id = id;
		this.gid = gid;
		this.score = score;
	}


	public int getId() {
		return id;
	}


	


	public int getGid() {
		return gid;
	}


	

	public int getScore() {
		return score;
	}


	
	
	
	

}
